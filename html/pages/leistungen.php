                Auf Grund der ständigen Änderungen in Form von neuen Gesetzen, Verwaltungsanweisungen und Urteilen kann das deutsche Steuerrecht vom Steuerpflichtigen selber oft nicht mehr überblickt werden.
                Es ist daher in vielen Fällen sinnvoll, sich entsprechend beraten zu lassen.
                Eine <strong>Hilfeleistung in Steuersachen</strong> darf in Deutschland ausschließlich von Personen und Vereinigungen erbracht werden, die hierzu ausdrücklich <strong>befugt sind</strong> (§ 2 StBerG). Dabei spielt es keine Rolle, ob die Hilfeleistung hauptberuflich oder nebenberuflich, entgeltlich oder unentgeltlich erbracht wird. Da im Falle einer unerlaubten Hilfeleistung in Steuersachen dem Steuerpflichtigen enorme finanzielle Nachteile entstehen können (z.B. bei einer Falschberatung oder bei Fristversäumnissen besteht kein Versicherungsschutz einer sonst verpflichtend vorgeschriebenen Berufshaftpflichtversicherung), werden derartige Gesetzesverstöße von den Behörden meist mit empfindlichen Sanktionen geahndet. </p>
